$('.price_once').on('click', function(){
    if($('.price_once').attr('id')!='price__menu__button__active'){
        $('.price_once_in_week').removeAttr('id');
        $('.price_once_in_2weeks').removeAttr('id');
        $('.price_once_a_month').removeAttr('id');
        $('.price_once').attr('id','price__menu__button__active')
        $('#price_appartments_price_1rooms').html('39 BYN');
        $('#price_appartments_price_2rooms').html('53 BYN');
        $('#price_appartments_price_3rooms').html('67 BYN');
    }
});

