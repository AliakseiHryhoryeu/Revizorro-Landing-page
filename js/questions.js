$('#showQuestionsContainer').on('click', function(){
//   $('.question_block_container').toggleClass('question_block_container2');
if($('.question_block_container').css('display')==='none'){
    $('.question_block_container').css('display','block');
$('.question_active_block_container_txt').html('Скрыть вопросы');
}else{
    $('.question_block_container').css('display','none');
    $('.question_active_block_container_txt').html('Все вопросы');
}
});

